// Оператор spread для строк и чисел в JavaScript **



// Задача №9


let arr = [...'12345'];
console.log(arr); // ["1", "2", "3", "4", "5"]









// Задача №10


let arr = ['a', ...'12345'];
console.log(arr); // ['a', '1', '2', '3', '4', '5']












// Задача №11



let arr = [...'12345', ...'56789'];
console.log(arr); // ['1', '2', '3', '4', '5', '6', '7', '8', '9']














// Задача №12


let arr1 = ['a', 'b', 'c'];
let arr2 = [...arr1, ...'12345'];

console.log(arr2); // ['a', 'b', 'c', '1', '2', '3', '4', '5']











