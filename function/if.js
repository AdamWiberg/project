// Логические операторы без if в функциях JavaScript


function func (a, b) {
    return a == b
}

console.log(func(1, 2))



function func (a, b) {
    return a != b;
}

console.log(func(1, 1))





function func(a, b) {
    return a + b >= 10;
}

console.log(func(1, 11))






function func(num) {
    return num >= 0
}

console.log(func(-2))

